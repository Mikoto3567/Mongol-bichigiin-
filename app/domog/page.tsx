interface HistoryCard {
  title: string;
  description: string;
  source?: string;
}

function ExpandableText({
  text,
  limit = 100,
}: {
  text: string;
  limit?: number;
}) {
  const [isExpanded, setIsExpanded] = useState(false);

  if (!text) return null;
  if (text.length <= limit)
    return <p className="text-sm text-[#7a6f62]">{text}</p>;

  return (
    <p className="text-sm text-[#7a6f62] leading-relaxed">
      {isExpanded ? text : `${text.substring(0, limit)}...`}
      <button
        onClick={(e) => {
          e.stopPropagation();
          setIsExpanded(!isExpanded);
        }}
        className="ml-2 text-[#5a4f42] font-bold hover:underline"
      >
        {isExpanded ? " Хумих" : "Цааш"}
      </button>
    </p>
  );
}

